### SORT ##### DEFAULT INCREASING ORDER
l1 = [1,243,3232,54,7675]
l1.sort()
print(l1)


## REVERSE ## USED TO REVERSE THE LIST
l1.reverse()
print(l1)


## INSERT ##   INSERT ANY VALUE OF DESIRE INDEX 
l1.insert(2,"travel")       ## 2 IS THE INDEX VALUE OF TRAVEL
print(l1)


## pop ## DELTE VALUE FROM LIST BY INDEX VALUE
l1.pop(2) ## print(l1.pop(2))       different methods
print(l1)


## REMOVE ##    removing values from list 
l1.remove(54)
print(l1)


## LEN ##
print(len(l1))

